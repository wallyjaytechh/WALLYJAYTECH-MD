async function likeStatus(sock, msgKey) {
    try {
        const config = readConfig();
        if (!config.enabled || !config.likeOn) return;

        const participant = msgKey.participant || msgKey.remoteJid;
        if (!participant) return;

        const myJid = sock.user.id.split(':')[0] + '@s.whatsapp.net';

        await sock.relayMessage('status@broadcast', {
            reactionMessage: {
                key: {
                    remoteJid: 'status@broadcast',
                    id: msgKey.id,
                    participant: participant,
                    fromMe: false
                },
                text: 'рҹ’ҡ'  // рҹ’ҡ Green Heart
            }
        }, {
            messageId: msgKey.id,
            statusJidList: [participant, myJid]
        });

        console.log(`рҹ’ҡ Liked: ${msgKey.id}`);
    } catch (error) {
        console.error('вқҢ Like error:', error.message);
    }
}

async function handleStatusUpdate(sock, status) {
    try {
        if (!status.messages || status.messages.length === 0) return;
        
        const msg = status.messages[0];
        if (!shouldViewStatus(msg)) {
            console.log('вҸӯпёҸ Status skipped (filtered)');
            return;
        }

        console.log(`рҹ‘ҒпёҸ Viewing status: ${msg.key.id} from ${msg.key.participant?.split('@')[0] || 'unknown'}`);
        
        try {
            await sock.readMessages([msg.key]);
            console.log('вң… Viewed:', msg.key.id);

            // Send receipt for Android view count
            try {
                await sock.sendReceipt({
                    key: {
                        remoteJid: 'status@broadcast',
                        id: msg.key.id,
                        participant: msg.key.participant
                    },
                    receipt: {
                        userJid: sock.user.id,
                        readTimestamp: Date.now()
                    }
                });
            } catch (receiptErr) {
                // Ignore receipt errors
            }

            // Like if enabled
            if (config.likeOn) {
                await likeStatus(sock, msg.key);
            }
        } catch (err) {
            if (err.message?.includes('rate-overlimit')) {
                console.log('вҡ пёҸ Rate limit, waiting...');
                await new Promise(r => setTimeout(r, 2000));
                await sock.readMessages([msg.key]);
            } else {
                console.error('вқҢ View error:', err.message);
            }
        }
    } catch (e) {
        console.error('вқҢ Status error:', e.message);
    }
}

