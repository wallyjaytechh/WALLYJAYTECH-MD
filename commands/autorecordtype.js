                    text: `вҡ пёҸ *ALREADY SET*\n\nв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\nвҸұпёҸ Duration is already *${d} seconds*.\n\nрҹ’Ў Use .autorecordtype duration <new value> to change.`,
