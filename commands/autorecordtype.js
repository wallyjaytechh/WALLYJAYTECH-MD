// COMMAND HANDLER
// в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ

async function autorecordtypeCommand(sock, chatId, message) {
    try {
        const senderId = message.key.participant || message.key.remoteJid;
        const isOwner = await isOwnerOrSudo(senderId, sock, chatId);
        if (!message.key.fromMe && !isOwner) {
            await sock.sendMessage(chatId, { text: 'вқҢ This command is only available for the owner!', ...channelInfo });
            return;
        }

        const userMessage = message.message?.conversation || message.message?.extendedTextMessage?.text || '';
        let cp = userMessage.trim();
        if (cp.startsWith('.')) cp = cp.substring(1);
        const parts = cp.split(/\s+/);
        const args = parts.slice(1);
        const config = initConfig();

        if (args.length === 0) {
            const status = config.enabled ? 'вң… ENABLED' : 'вқҢ DISABLED';
            const statusIcon = config.enabled ? 'рҹҹў' : 'рҹ”ҙ';
            const filterMode = config.numberList.length > 0 ? (config.includeMode ? 'вң… Include Only' : 'рҹҡ« Exclude') : 'рҹҢҚ All Numbers';
            const activeCount = activeSessions.size;

            await sock.sendMessage(chatId, {
                text: `рҹҺҷпёҸвҢЁпёҸ *AUTO-RECORD-TYPE SETTINGS*\n\n` +
                      `в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\n` +
                      `${statusIcon} *Status:* ${status}\n` +
                      `рҹҺҜ *Mode:* ${getModeText(config.mode)}\n` +
                      `вҸұпёҸ *Duration:* ${config.infinite ? 'вҷҫпёҸ Infinite' : config.duration + 's'}\n` +
                      `вҷҫпёҸ *Infinite:* ${config.infinite ? 'ON' : 'OFF'}\n` +
                      `рҹ”ў *Filter:* ${filterMode} (${config.numberList.length} numbers)\n` +
                      `рҹ”„ *Active Sessions:* ${activeCount}\n\n` +
                      `в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\n` +
                      `рҹ“– *Commands:*\n` +
                      `в”” .autorecordtype on - Enable both\n` +
                      `в”” .autorecordtype off - Disable both\n` +
                      `в”” .autorecordtype mode all/dms/groups\n` +
                      `в”” .autorecordtype duration <seconds>\n` +
                      `в”” .autorecordtype duration infinite\n` +
                      `в”” .autorecordtype status - Show settings\n` +
                      `в”” .autorecordtype include add <numbers>\n` +
                      `в”” .autorecordtype include remove <numbers>\n` +
                      `в”” .autorecordtype exclude add <numbers>\n` +
                      `в”” .autorecordtype exclude remove <numbers>\n` +
                      `в”” .autorecordtype includelist / excludelist\n` +
                      `в”” .autorecordtype includeclear / excludeclear\n\n` +
                      `в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\n` +
                      `рҹ”„ *Alternates recording/typing every 5 seconds*\n` +
                      `рҹҺҷпёҸ *Starts with RECORDING first*\n` +
                      `вң… *Individual auto-typing/recording ENABLED & SYNCED for each message*\n\n` +
                      `рҹ’Ў *Examples:*\n` +
                      `в”” .autorecordtype duration infinite\n` +
                      `в”” .autorecordtype include add 2347012345678`,
                ...channelInfo
            });
            return;
        }

        const action = args[0].toLowerCase();

        if (action === 'on' || action === 'enable') {
            if (config.enabled) {
                await sock.sendMessage(chatId, {
                    text: `вҡ пёҸ *ALREADY ENABLED*\n\nв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\nрҹҺҷпёҸвҢЁпёҸ Auto-Record-Type is already *ON*.\nрҹҺҜ Mode: ${getModeText(config.mode)}\nвҸұпёҸ Duration: ${config.infinite ? 'вҷҫпёҸ Infinite' : config.duration + 's'}\n\nрҹ’Ў Use .autorecordtype off to disable.`,
                    ...channelInfo
                });
                return;
            }
            
            config.enabled = true;
            fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
            
            await enableIndividualHandlers();
            
            await sock.sendMessage(chatId, {
                text: `вң… *AUTO-RECORD-TYPE ENABLED*\n\n` +
                      `в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\n` +
                      `рҹҺҜ Mode: ${getModeText(config.mode)}\n` +
                      `вҸұпёҸ Duration: ${config.infinite ? 'вҷҫпёҸ Infinite' : config.duration + 's'}\n\n` +
                      `вң… Auto-typing: ENABLED\n` +
                      `вң… Auto-record: ENABLED\n` +
                      `рҹ”„ Alternating every 5 seconds\n` +
                      `рҹҺҷпёҸ Starting with RECORDING\n\n` +
                      `рҹ“Ң Both indicators active in ${getModeDescription(config.mode)}`,
                ...channelInfo
            });
            
            await startAlternatingSession(sock, chatId, config.duration, config.infinite);
        }
        else if (action === 'off' || action === 'disable') {
            if (!config.enabled) {
                await sock.sendMessage(chatId, {
                    text: `вҡ пёҸ *ALREADY DISABLED*\n\nв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\nрҹҺҷпёҸвҢЁпёҸ Auto-Record-Type is already *OFF*.\n\nрҹ’Ў Use .autorecordtype on to enable.`,
                    ...channelInfo
                });
                return;
            }
            const stopped = stopAllAlternatingSessions();
            config.enabled = false;
            config.infinite = false;
            config.duration = DEFAULT_DURATION;
            fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
            await disableBoth();
            
            await sock.sendMessage(chatId, {
                text: `вқҢ *AUTO-RECORD-TYPE DISABLED*\n\n` +
                      `в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\n` +
                      `рҹӣ‘ Both typing & recording stopped.\n` +
                      `рҹ”„ Stopped ${stopped} active session(s).\n` +
                      `вҸұпёҸ Duration reset to ${DEFAULT_DURATION}s.\n\n` +
                      `рҹ’Ў Use .autorecordtype on to enable.`,
                ...channelInfo
            });
        }
        else if (action === 'mode') {
            if (args.length < 2) {
                await sock.sendMessage(chatId, { text: `вҡ пёҸ *USAGE:* .autorecordtype mode <all/dms/groups>`, ...channelInfo });
                return;
            }
            const mode = args[1].toLowerCase();
            if (mode === 'all' || mode === 'dms' || mode === 'groups') {
                if (config.mode === mode) {
                    await sock.sendMessage(chatId, { text: `вҡ пёҸ *ALREADY SET*\n\nрҹҺҜ Mode is already *${getModeText(mode)}*.`, ...channelInfo });
                    return;
                }
                config.mode = mode;
                fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
                await syncConfigToIndividual(mode, config.duration, config.infinite, config.includeMode, config.numberList);
                await sock.sendMessage(chatId, {
                    text: `рҹҺҜ *MODE UPDATED*\n\nв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\nв”” New mode: ${getModeText(mode)}\n\nрҹ“Ң ${getModeDescription(mode)}`,
                    ...channelInfo
                });
            } else {
                await sock.sendMessage(chatId, { text: `вҡ пёҸ *INVALID MODE*\n\nрҹ“– Available: all, dms, groups`, ...channelInfo });
            }
        }
        else if (action === 'duration') {
            if (args.length < 2) {
                await sock.sendMessage(chatId, { text: `вҡ пёҸ *USAGE:* .autorecordtype duration <seconds> or infinite`, ...channelInfo });
                return;
            }
            if (args[1].toLowerCase() === 'infinite') {
                if (config.infinite) {
                    await sock.sendMessage(chatId, { text: `вҡ пёҸ *ALREADY INFINITE*`, ...channelInfo });
                    return;
                }
                config.infinite = true;
                config.duration = DEFAULT_DURATION;
                fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
                await syncConfigToIndividual(config.mode, config.duration, true, config.includeMode, config.numberList);
                await sock.sendMessage(chatId, {
                    text: `вҷҫпёҸ *INFINITE MODE ENABLED*\n\nв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\nрҹ“Ң Both typing & recording will alternate every 5 seconds indefinitely.\nрҹҺҷпёҸ Starting with RECORDING`,
                    ...channelInfo
                });
                if (config.enabled) {
                    stopAllAlternatingSessions();
                    await startAlternatingSession(sock, chatId, config.duration, true);
                }
                return;
            }
            const d = parseInt(args[1]);
            if (isNaN(d) || d < 5 || d > 120) {
                await sock.sendMessage(chatId, { text: `вҡ пёҸ *INVALID DURATION*\n\nрҹ“Ң 5-120 seconds or use 'infinite'.`, ...channelInfo });
                return;
            }
            if (config.duration === d && !config.infinite) {
                await sock.sendMessage(chatId, { text: `вҡ пёҸ *ALREADY SET*\n\nвҸұпёҸ Duration is already *${d}s*.`, ...channelInfo });
                return;
            }
            config.duration = d;
            config.infinite = false;
            fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
            await syncConfigToIndividual(config.mode, d, false, config.includeMode, config.numberList);
            stopAllAlternatingSessions();
            await sock.sendMessage(chatId, {
                text: `вҸұпёҸ *DURATION UPDATED*\n\nв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\nв”” Both typing & recording: ${d} seconds\nв”” Infinite mode: OFF`,
                ...channelInfo
            });
            if (config.enabled) {
                await startAlternatingSession(sock, chatId, d, false);
            }
        }
        else if (action === 'include') {
            const sub = args[1]?.toLowerCase();
            if (sub === 'add') {
                const numbers = args.slice(2).join(' ').split(/[, ]+/).map(n => n.replace(/[^0-9]/g, '')).filter(n => n.length >= 7);
                if (numbers.length === 0) {
                    await sock.sendMessage(chatId, { text: `вҡ пёҸ *USAGE:* .autorecordtype include add 2347012345678`, ...channelInfo });
                    return;
                }
                config.includeMode = true;
                for (const num of numbers) { if (!config.numberList.includes(num)) config.numberList.push(num); }
                fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
                await syncConfigToIndividual(config.mode, config.duration, config.infinite, true, config.numberList);
                await sock.sendMessage(chatId, { text: `вң… *INCLUDE ADDED*\n\nрҹ“Ң Mode: Include Only\nрҹ”ў Added ${numbers.length} number(s)`, ...channelInfo });
            }
            else if (sub === 'remove') {
                const numbers = args.slice(2).join(' ').split(/[, ]+/).map(n => n.replace(/[^0-9]/g, '')).filter(n => n.length >= 7);
                if (numbers.length === 0) { await sock.sendMessage(chatId, { text: `вҡ пёҸ *USAGE:* .autorecordtype include remove <numbers>`, ...channelInfo }); return; }
                const before = config.numberList.length;
                config.numberList = config.numberList.filter(n => !numbers.includes(n));
                fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
                await syncConfigToIndividual(config.mode, config.duration, config.infinite, true, config.numberList);
                await sock.sendMessage(chatId, { text: `вң… *INCLUDE REMOVED*\n\nрҹ“Ң Removed ${before - config.numberList.length} number(s).`, ...channelInfo });
            }
            else { await sock.sendMessage(chatId, { text: `рҹ“Ӣ *INCLUDE MODE*\n\nрҹ”ў Numbers: ${config.numberList.length}`, ...channelInfo }); }
        }
        else if (action === 'exclude') {
            const sub = args[1]?.toLowerCase();
            if (sub === 'add') {
                const numbers = args.slice(2).join(' ').split(/[, ]+/).map(n => n.replace(/[^0-9]/g, '')).filter(n => n.length >= 7);
                if (numbers.length === 0) {
                    await sock.sendMessage(chatId, { text: `вҡ пёҸ *USAGE:* .autorecordtype exclude add 2347012345678`, ...channelInfo });
                    return;
                }
                config.includeMode = false;
                for (const num of numbers) { if (!config.numberList.includes(num)) config.numberList.push(num); }
                fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
                await syncConfigToIndividual(config.mode, config.duration, config.infinite, false, config.numberList);
                await sock.sendMessage(chatId, { text: `вң… *EXCLUDE ADDED*\n\nрҹ“Ң Mode: Exclude\nрҹ”ў Added ${numbers.length} number(s)`, ...channelInfo });
            }
            else if (sub === 'remove') {
                const numbers = args.slice(2).join(' ').split(/[, ]+/).map(n => n.replace(/[^0-9]/g, '')).filter(n => n.length >= 7);
                if (numbers.length === 0) { await sock.sendMessage(chatId, { text: `вҡ пёҸ *USAGE:* .autorecordtype exclude remove <numbers>`, ...channelInfo }); return; }
                const before = config.numberList.length;
                config.numberList = config.numberList.filter(n => !numbers.includes(n));
                fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
                await syncConfigToIndividual(config.mode, config.duration, config.infinite, false, config.numberList);
                await sock.sendMessage(chatId, { text: `вң… *EXCLUDE REMOVED*\n\nрҹ“Ң Removed ${before - config.numberList.length} number(s).`, ...channelInfo });
            }
            else { await sock.sendMessage(chatId, { text: `рҹ“Ӣ *EXCLUDE MODE*\n\nрҹ”ў Numbers: ${config.numberList.length}`, ...channelInfo }); }
        }
        else if (action === 'includelist') {
            const nums = config.numberList;
            await sock.sendMessage(chatId, { text: `рҹ“Ӣ *INCLUDE LIST*\n\nрҹ”ў Mode: Include Only\nрҹ“Ҡ Total: ${nums.length}\n\n${nums.map((n, i) => `${i + 1}. +${n}`).join('\n')}`, ...channelInfo });
        }
        else if (action === 'excludelist') {
            const nums = config.numberList;
            await sock.sendMessage(chatId, { text: `рҹ“Ӣ *EXCLUDE LIST*\n\nрҹ”ў Mode: Exclude\nрҹ“Ҡ Total: ${nums.length}\n\n${nums.map((n, i) => `${i + 1}. +${n}`).join('\n')}`, ...channelInfo });
        }
        else if (action === 'includeclear') {
            if (config.numberList.length === 0) { await sock.sendMessage(chatId, { text: `вҡ пёҸ *ALREADY EMPTY*`, ...channelInfo }); return; }
            config.numberList = [];
            fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
            await syncConfigToIndividual(config.mode, config.duration, config.infinite, config.includeMode, []);
            await sock.sendMessage(chatId, { text: `вң… *INCLUDE LIST CLEARED*`, ...channelInfo });
        }
        else if (action === 'excludeclear') {
            if (config.numberList.length === 0) { await sock.sendMessage(chatId, { text: `вҡ пёҸ *ALREADY EMPTY*`, ...channelInfo }); return; }
            config.numberList = [];
            fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
            await syncConfigToIndividual(config.mode, config.duration, config.infinite, config.includeMode, []);
            await sock.sendMessage(chatId, { text: `вң… *EXCLUDE LIST CLEARED*`, ...channelInfo });
        }
        else if (action === 'status') {
            const filterMode = config.numberList.length > 0 ? (config.includeMode ? 'вң… Include Only' : 'рҹҡ« Exclude') : 'рҹҢҚ All Numbers';
            const activeCount = activeSessions.size;
            await sock.sendMessage(chatId, {
                text: `рҹҺҷпёҸвҢЁпёҸ *AUTO-RECORD-TYPE STATUS*\n\n` +
                      `в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\n` +
                      `${config.enabled ? 'рҹҹў ENABLED' : 'рҹ”ҙ DISABLED'}\n` +
                      `рҹҺҜ Mode: ${getModeText(config.mode)}\n` +
                      `вҸұпёҸ Duration: ${config.infinite ? 'вҷҫпёҸ Infinite' : config.duration + 's'}\n` +
                      `вҷҫпёҸ Infinite: ${config.infinite ? 'ON' : 'OFF'}\n` +
                      `рҹ”ў Filter: ${filterMode} (${config.numberList.length})\n` +
                      `рҹ”„ Sessions: ${activeCount}`,
                ...channelInfo
            });
        }
        else {
            await sock.sendMessage(chatId, { text: `вҡ пёҸ *INVALID COMMAND*\n\nрҹ“– Use .autorecordtype to see all options.`, ...channelInfo });
        }
    } catch (error) { console.error('вқҢ Error:', error); }
}

